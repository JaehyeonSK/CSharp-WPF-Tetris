    public class Block
    {
        public Block(int x, int y, int shape, Color color, int rotate = 0)
        {
            this.X = x;
            this.Y = y;
            this.ShapeNumber = shape;
            this.Color = color;
            this.Rotate = rotate;

            this.Shape = new int[BlockShape.Shapes.GetLength(2), BlockShape.Shapes.GetLength(1)];
            for (int bx = 0; bx < Shape.GetLength(1); bx++)
            {
                for (int by = 0; by < Shape.GetLength(0); by++)
                {
                    Shape[by, bx] = BlockShape.Shapes[ShapeNumber, Rotate, by, bx];
                }
            }
            
        }

        
        public int[,] Shape;

        public int X { get; set; }
        public int Y { get; set; }
        public int ShapeNumber { get; set; }
        public int Rotate { get; }
        public Color Color { get; set; }

    }
